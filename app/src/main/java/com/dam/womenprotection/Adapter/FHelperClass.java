package com.dam.womenprotection.Adapter;

public class FHelperClass {

    int image;
    String title;
    String descrip;

    public FHelperClass(int image, String title, String descrip) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }

    public String getDescrip() {
        return descrip;
    }


}
